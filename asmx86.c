	static int64_t ReadFinalOpSize(DecodeState* state)
	{
		if (state->flags & DEC_FLAG_IMM_SX)
			return ReadSigned8(state);
		switch (state->finalOpSize)
		{
		case 1:
			return Read8(state);
		case 2:
			return Read16(state);
		case 4:
			return Read32(state);
		case 8:
			return ReadSigned32(state);
		}
		return 0;
	}


